function getLetter(s) {
    switch (s[0]) {
        case 'a':
        case 'i':
        case 'o':
        case 'u':
           console.log("A");
            break;
        case 'b':
        case 'c':
        case 'd':
        case 'f':
        case 'g':
           console.log("B");
            break;
        case 'h':
        case 'j':
        case 'k':
        case 'l':
        case 'm':
           console.log("C");
            break;

    }   
} 
